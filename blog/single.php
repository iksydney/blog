                    <p>
                        Despite Agribusiness being one of the most interesting and rewarding professions/businesses in 
                        the world, it remains quite unrecognized by many. However, in recent times, youths have been tapping 
                        into the opportunity of agriculture, exploring productive and profitable opportunities therein. 
                        Agribusiness has been defined by many as the business of profitable agriculture. 
                    </p>
    
                    <p>
                        One key thing that has influenced the attitude of people, especially Youth, in developing and underdeveloped
                        countries is mindset. Mindset is personal and refers to the set of belief, aura collection of thoughts and 
                        beliefs that shape people’s thought habits. This affects how people think, and what they feel towards a thing, 
                        person, place, situation, or topic. Nigeria in this case, is said to be deficient of proactive human labor, 
                        which boils down to the mindset they have towards farming and agriculture as being a profession of the “poor, 
                        illiterate and/or aged generation”. Across pages of magazines, textbooks, and dailies, we find agriculture and 
                        farming portrayed as lowly and poorly, with photos of aged, poor-looking, and even malnourished men, women, and 
                        children, farming in the most subsistence and crude manner. Little wonder why “trendy” youths and “millennials” 
                        have been put off farming and agriculture for some time now. A business mindset and approach towards farming and 
                        agriculture is beginning to change the narrative, but how effectively, and how far gone are we in this new perspective?
                    </p>
                    
                    <p>
                    Agribusiness is said to be the combination of the words "agriculture" and "business" and refers to any business 
                    related to farming and farming-related commercial activities; this can be further described as a group of industries 
                    dealing with agricultural produce and services required in farming. In today’s world, we have people especially youths 
                    getting involved in one sector or niche of the agricultural sector mainly because of its“ rumored” profitability, 
                    only to get discouraged when they don’t get the calculated or expected, “mind-blowing” profit. In view of this, 
                    we shall quickly glance at some various negative mindsets people have towards agriculture and agribusiness in general, 
                    whilst exploring the attitudes of youths to agricultural development and the effect of this mindset on food security 
                    and sufficiency in Nigeria. This also applies mostly throughout sub-Saharan Africa.
                    </p>
                    <p>
                        One of the major mindsets towards agribusiness in Nigeria is that agribusiness is dirty. One of the places our 
                        mindset and mentality against agribusiness has failed us is where we have the belief that agribusiness can’t be 
                        identified with because of the dirty nature. When youths especially ask me about this or make such comments, 
                        I ask them what exactly “dirty” means? Literally most jobs have their dirty bits, which is why you see surgeons 
                        in the making, go sick and sometimes throw-up at every first time they are asked to work on caravan; bankers alike 
                        must face agitated customers every now and then, tech enthusiast must build or program solutions to world problems. 
                        Artistes and entertainers are faced with media negativity, teachers are faced with bullying and stubborn students 
                        that shred their dignity for no reason. All professions have their“ dirtiness” which I prefer to call “occupational 
                        risk”, and we need to embrace the uniqueness of our profession (agribusiness) including its risks to bridge the gap 
                        between the world of hunger, malnourishment, food insecurity and the world of nourishment and food security. 
                        The world population is estimated by experts increase by over 30% by 2030, if our mindset is not positively 
                        regulated, famine might be the order of the day. The orientation towards farming and its business must be changed 
                        and modernized accordingly. <blockquote>(To be continued)</blockquote>
                    </p>
